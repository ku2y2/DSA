#include<bits/stdc++.h>
using namespace std;
int main()
{
    //for unsigned number
    unsigned int a = 5;
    unsigned int d = 2;
    int b = 5;
    int c =2;
    int e = -8;
    cout<< "Bitwise not of unsigned int a = "<<(~a)<<endl;
    cout<< "Bitwise not of unsigned int d = "<<(~d)<<endl;
    cout<< "Bitwise not of signed int b = "<<(~b)<<endl;
    cout<< "Bitwise not of signed int c = "<<(~c)<<endl;
    cout<< "Bitwise not of signed int e = "<<(~e)<<endl;
}